# Aggregation Function : Means To Get Summarary From Data

import numpy as np 

lst = [10,20,30]

a = np.array([lst])
print(np.sum(a)) # Sum Of All Array Elements
print(np.mean(a))# Mean Of All Array Elements
print(np.min(a))# Minimum Of All Array Elements
print(np.max(a))# Maxmium Of All Array Elements
print(np.std(a))# Standard Deviation Of All Array Elements
print(np.var(a))# Variance Of All Array Elements

